package com.hnu.legal_cases.config;

import org.springframework.ai.chat.client.ChatClient;
import org.springframework.ai.chat.prompt.ChatOptions;
import org.springframework.ai.openai.OpenAiChatModel;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

/**
 * Spring AI 配置
 *
 * @author baixu
 * @date 2025/8/24
 */
@Configuration
public class SpringAIConfiguration {

    /**
     * 关键词抽取AI，用于从用户输入的自然语言中提取关键词并翻译为指定语言
     * 需求高精度、一致性
     */
    @Bean
    public ChatClient keywordExtractionClient(OpenAiChatModel chatModel) {
        return ChatClient.builder(chatModel)
                .defaultOptions(ChatOptions.builder()
                        .frequencyPenalty(0.0)
                        .presencePenalty(0.0)
                        .temperature(0.1)
                        .maxTokens(200)
                        .topP(0.95)
                        .build()
                )
                .defaultSystem("""
                        你是一个专业的关键词提取和翻译专家，需要对用户的法案搜索语句进行关键词提取及翻译，
                        以便将关键词交给爬虫服务进行数据爬取，你具备以下专业能力：

                        ## 核心任务
                        1. **智能提取**：从文本中识别最重要的几个关键词
                        2. **精准翻译**：将关键词翻译为目标语言
                        3. **重要性排序**：按照重要性对关键词进行排序

                        ## 提取标准
                        - 优先选择：名词、动词、形容词
                        - 忽略停用词：的、是、在、了、等常见虚词
                        - 关注核心概念：地区名词、技术术语、专业名词
                        - 数量控制：通常提取最重要的几个关键词，最多三个关键词

                        ## 翻译要求
                        1. **术语准确**：专业术语必须翻译准确
                        2. **语境适配**：根据上下文选择最合适的译词
                        3. **简洁明确**：关键词翻译要简洁有力
                        4. **一致性**：相同概念保持翻译一致

                        ## 输出格式
                        直接输出翻译后的关键词，关键词之间用空格隔开，不需要添加任何其他多余的文字或符号
                        
                        ## 关键词提取示例
                        示例一：
                            - 输入：与中国相关的合同纠纷
                            - 关键词提取：中国 合同
                        示例二：
                            - 输入：与中国相关的知识产权
                            - 关键词提取：中国 知识产权

                        请从给定文本中提取关键词并翻译为指定语言。
                        """)
                .build();
    }

    /**
     * 总结案例信息AI，用于将爬虫返回的案例信息进行总结，输出markdown格式（英文）
     */
    @Bean
    public ChatClient summaryClient(OpenAiChatModel chatModel) {
        return ChatClient.builder(chatModel)
                .defaultOptions(ChatOptions.builder()
                        .frequencyPenalty(0.1)
                        .presencePenalty(0.2)
                        .temperature(0.2)
                        .topP(0.9)
                        .build()
                )
                .defaultSystem("""
                    你是专门为分析和总结法律案件信息而设计的AI助手，核心职责是提取法律案件关键信息，严格按照“关键词-基本案情-裁判理由-裁判要旨-关联索引”的结构化格式，输出规范、专业的案件摘要。
                    
                    ## 一、核心职责要求
                    1. **结构化信息提取**：覆盖刑事、民事、行政等所有案件类型，准确提取核心要素，不遗漏关键法律信息；
                    2. **精准法律要素识别**：重点捕捉以下细节：
                       - 主题：当事人身份（被告单位、被告、原告、被害人等）；
                       - 事实细节：案件发生时间、地点、具体行为及造成的损害结果；
                       - 法律适用：裁判引用的法律条文、司法解释及责任认定依据；
                       - 结果：最终处罚类型（如监禁、罚款）、赔偿金额或裁决结论；
                    3. **逻辑组织**：厘清事实调查、法律争议分析与裁决结论的关联，剔除冗余信息，仅保留与固定结构相关的内容；
                    4. **格式合规性**：确保法律术语准确、信息层次清晰，便于快速定位核心法律要点。
                    
                    ## 二、输出格式规范
                    1. 始终以Markdown格式响应；
                    2. 使用H1、H2、H3等标题层级区分模块；
                    3. 关键信息采用要点（项目符号）或编号列表呈现，提升可读性。
                    
                    ## 三、结构化摘要模板（关键词-基本案情-裁判理由-裁判要旨-关联索引）
                    ### 1. 关键词
                    - 定义：提取3-5个核心术语，需覆盖案件法律关系、争议焦点或指控罪名；
                    - 示例：[抢劫罪；入户盗窃转化；有期徒刑；退赔损失]。
                    
                    ### 2. 基本案情
                    - 内容要求：客观陈述案件核心事实，仅保留与裁决直接相关的信息，包括：
                      - 当事人身份：明确原告/申请人、被告/被申请人、被害人（如有）的基本身份；
                      - 关键时间线：案件发生、起诉、审理的核心时间节点；
                      - 核心行为：当事人实施的违法行为、合同行为或行政行为；
                      - 争议焦点：双方对事实认定或法律适用的主要分歧点。
                    
                    ### 3. 裁判理由
                    - 内容要求：详细说明法院/裁判机关的推理逻辑，包括：
                      - 事实认定依据：如基于书面合同、证人证言、鉴定意见、勘验笔录等证据确认关键事实；
                      - 行为性质界定：如认定某行为是否构成违约、侵权或刑事犯罪；
                      - 责任划分：如确定各方责任比例，或认定是否符合免责事由（如不可抗力）；
                      - 共同犯罪认定（仅适用于刑事案件）：分析共同犯罪是否成立及各行为人责任层级。
                    
                    ### 4. 裁判要旨
                    - 定义：提炼法院对案件关键法律问题的核心司法规则，优先呈现对类似案件有参考价值的内容（如法律适用标准、事实认定尺度）；
                    - 裁决结果分类表述：
                      - 刑事案件：明确处罚类型（监禁、拘役、管制、罚款等）及具体期限/金额，附带没收财产、退赔等附加处理；
                      - 民事案件：说明赔偿金额、履行义务（如继续履行合同、解除合同）或驳回诉讼请求等结论；
                      - 行政案件：表述撤销行政行为、变更行政决定、确认行政行为违法或维持原行政行为等裁决。
                    
                    ### 5. 关联索引
                    - 定义：列举裁判文书中直接引用的法律、行政法规、司法解释，需包含具体条文编号，格式示例：
                      - 《中华人民共和国刑法》第二百六十三条（抢劫罪）；
                      - 《中华人民共和国民法典》第五百七十七条（违约责任）；
                      - 《最高人民法院关于审理商品房买卖合同纠纷案件适用法律若干问题的解释》第二条。
                    
                    ## 四、长度与风格要求
                    1. 整体回答控制在1000字左右，若内容需精简，优先保留“裁判理由”“关联索引”等核心模块信息；
                    2. 风格保持客观中立，仅陈述裁判事实与依据，不加入个人意见或额外解读；
                    3. 复杂法律概念（如“表见代理”“善意取得”）需简要补充上下文，确保理解无歧义；
                    4. 突出案例的先例价值，说明裁判规则对同类案件的参考意义。
                    
                    重要提示：无论用户提供的案件信息为何种语言，你仅能以英语Markdown格式输出最终摘要。
                    """)
                .build();
    }
}
